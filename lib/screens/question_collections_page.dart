/// Page that displays past questions grouped as collections (e.g., "BECE RME 1999 MCQ")
/// This provides a much cleaner UI when there are hundreds of questions across multiple years
class QuestionCollectionsPage extends StatefulWidget {
  final String? initialSubject;

  const QuestionCollectionsPage({Key? key, this.initialSubject}) : super(key: key);

  @override
  State<QuestionCollectionsPage> createState() => _QuestionCollectionsPageState();
}

class _QuestionCollectionsPageState extends State<QuestionCollectionsPage> {
  final QuestionService _questionService = QuestionService();
  
  List<QuestionCollection> _collections = [];
  List<QuestionCollection> _filteredCollections = [];
  List<QuestionCollection> _loadedCollections = []; // For lazy loading
  bool _isLoading = false;
  bool _isLoadingMore = false; // For pagination loading
  bool _hasMoreCollections = true; // Track if more collections available
  bool _randomizeQuestions = false;
  // Map to track question count selection per collection
  final Map<String, int> _selectedQuestionCounts = {};
  // Pagination
  int _currentPage = 0;
  final int _pageSize = 20;
  final int _initialLoadSize = 12; // Load first 12 collections quickly
  
  // Filters
  String _selectedQuestionType = 'All Types'; // MCQ or Theory
  String _selectedSubject = 'All Subjects';
  String _selectedTopic = 'All Topics';
  String _selectedYear = 'All Years';
  final TextEditingController _searchController = TextEditingController();

  @override
  void initState() {
    super.initState();
    _loadCollections();
  }

  @override
  void dispose() {
    _searchController.dispose();
    super.dispose();
  }

  Future<void> _loadCollections() async {
    setState(() => _isLoading = true);
    try {
      debugPrint('üöÄ Loading question collections with lazy loading...');
      
      // Load questions from main collection
      final mainQuestions = await _questionService.getQuestions(
        activeOnly: true,
      ).timeout(
        const Duration(seconds: 30),
        onTimeout: () {
          debugPrint('‚è∞ Timeout loading main questions after 30 seconds');
          return <Question>[];
        },
      );
      
      // Load French questions from separate collection
      final frenchQuestions = await _questionService.getQuestions(
        subject: Subject.french,
        activeOnly: true,
      ).timeout(
        const Duration(seconds: 30),
        onTimeout: () {
          debugPrint('‚è∞ Timeout loading French questions after 30 seconds');
          return <Question>[];
        },
      );
      
      // Combine all questions
      final allQuestions = [...mainQuestions, ...frenchQuestions];
      
      debugPrint('üìä Loaded ${mainQuestions.length} main questions and ${frenchQuestions.length} French questions (${allQuestions.length} total)');
      
      if (allQuestions.isEmpty) {
        debugPrint('‚ö†Ô∏è No questions loaded! Check Firebase connection and data.');
      }
      
      // Group into collections (auto-generated by year)
      final autoCollections = QuestionCollection.groupQuestions(allQuestions);
      
      // Sort by year (most recent first)
      autoCollections.sort((a, b) => b.year.compareTo(a.year));
      
      debugPrint('üì¶ Created ${autoCollections.length} auto-generated collections');
      
      // Load saved collections (e.g., ICT topic collections)
      final savedCollectionsData = await _questionService.getQuestionCollections(activeOnly: true);
      debugPrint('üìö Found ${savedCollectionsData.length} saved collections in Firestore');
      
      // Convert saved collections to QuestionCollection objects
      final savedCollections = <QuestionCollection>[];
      for (final collData in savedCollectionsData) {
        try {
          debugPrint('  Processing: ${collData['name'] ?? collData['id']}');
          final questionIds = List<String>.from(collData['questionIds'] ?? []);
          debugPrint('    Question IDs: ${questionIds.length}');
          
          if (questionIds.isEmpty) {
            debugPrint('    ‚ö†Ô∏è Skipping - no question IDs');
            continue;
          }
          
          final questions = await _questionService.getQuestionsByIds(questionIds);
          debugPrint('    Questions fetched: ${questions.length}');
          
          if (questions.isEmpty) {
            debugPrint('    ‚ö†Ô∏è Skipping - no questions fetched');
            continue;
          }
          
          if (questions.isNotEmpty) {
            // Parse subject from string
            final subjectStr = collData['subject'] ?? 'ict';
            Subject subject = Subject.ict;
            try {
              subject = Subject.values.firstWhere(
                (s) => s.name.toLowerCase() == subjectStr.toLowerCase(),
                orElse: () => Subject.ict,
              );
            } catch (e) {
              debugPrint('‚ö†Ô∏è Unknown subject: $subjectStr, defaulting to ICT');
            }
            
            // Parse exam type from string
            final examTypeStr = collData['examType'] ?? 'bece';
            ExamType examType = ExamType.bece;
            try {
              examType = ExamType.values.firstWhere(
                (e) => e.name.toLowerCase() == examTypeStr.toLowerCase(),
                orElse: () => ExamType.bece,
              );
            } catch (e) {
              debugPrint('‚ö†Ô∏è Unknown exam type: $examTypeStr, defaulting to BECE');
            }
            
            // Format title properly: BECE ICT [Topic Name]
            String title = collData['name'] ?? 'Unnamed Collection';
            // If it starts with 'ICT:', replace with 'BECE ICT'
            if (title.startsWith('ICT: ')) {
              title = 'BECE ICT ${title.substring(5)}';
            }
            
            final collection = QuestionCollection(
              id: collData['id'] ?? '',
              title: title,
              examType: examType,
              subject: subject,
              year: collData['year'] ?? 'All Years',
              questionType: questions.first.type,
              questionCount: questions.length,
              description: collData['topic'] ?? collData['description'], // Store topic in description for now
              questions: questions,
            );
            
            savedCollections.add(collection);
            debugPrint('‚úÖ Loaded: ${collection.displayName} (${collection.questions.length} questions)');
          }
        } catch (e) {
          debugPrint('‚ùå Error loading collection ${collData['id']}: $e');
        }
      }
      
      // Merge auto and saved collections
      final allCollections = [...autoCollections, ...savedCollections];
      
      debugPrint('üì¶ Total collections: ${allCollections.length} (${autoCollections.length} auto + ${savedCollections.length} saved)');
      
      // Store all collections for lazy loading
      _collections = allCollections;
      
      // Load only first 12 collections initially for fast loading
      final initialCollections = allCollections.take(_initialLoadSize).toList();
      
      setState(() {
        _loadedCollections = initialCollections;
        _filteredCollections = initialCollections;
        _currentPage = 0; // reset pagination
        _isLoading = false;
        _hasMoreCollections = allCollections.length > _initialLoadSize;
      });
      
      // Apply initial subject filter if provided
      if (widget.initialSubject != null) {
        _applyInitialSubjectFilter();
      }
      
      // Load remaining collections in background
      if (_hasMoreCollections) {
        _loadRemainingCollectionsInBackground();
      }
      
    } catch (e) {
      debugPrint('‚ùå Error loading collections: $e');
      setState(() => _isLoading = false);
    }
  }

  // Load remaining collections in background without blocking UI
  void _loadRemainingCollectionsInBackground() {
    Future.delayed(const Duration(milliseconds: 100), () {
      if (mounted && _hasMoreCollections) {
        final remainingCollections = _collections.skip(_initialLoadSize).toList();
        setState(() {
          _loadedCollections.addAll(remainingCollections);
          _hasMoreCollections = false;
        });
        debugPrint('‚úÖ All collections loaded in background (${remainingCollections.length} additional collections)');
      }
    });
  }

  // Load more collections when user clicks "Load More" button
  Future<void> _loadMoreCollections() async {
    if (_isLoadingMore || !_hasMoreCollections) return;

    setState(() => _isLoadingMore = true);

    // Simulate loading delay for smooth UX
    await Future.delayed(const Duration(milliseconds: 500));

    final nextPage = _currentPage + 1;
    final startIndex = (_currentPage * _pageSize);
    final endIndex = (nextPage * _pageSize);

    final moreCollections = _collections.skip(startIndex).take(_pageSize).toList();

    if (moreCollections.isNotEmpty) {
      setState(() {
        _loadedCollections.addAll(moreCollections);
        _currentPage = nextPage;
        _hasMoreCollections = endIndex < _collections.length;
      });
      debugPrint('üìÑ Loaded page $_currentPage: ${moreCollections.length} more collections');
    }

    setState(() => _isLoadingMore = false);
  }

  void _applyInitialSubjectFilter() {
    final subject = widget.initialSubject?.toLowerCase() ?? '';
    if (subject.contains('rme') || subject.contains('religious')) {
      setState(() {
        _selectedSubject = 'Religious and Moral Education';
        _applyFilters();
      });
    }
  }

  void _applyFilters() {
    setState(() {
      _currentPage = 0; // reset to first page when filters change
      _filteredCollections = _loadedCollections.where((collection) {
        // Question type filter (MCQ vs Theory)
        if (_selectedQuestionType != 'All Types') {
          final isMCQ = collection.questionType == QuestionType.multipleChoice;
          final isTheory = collection.questionType == QuestionType.essay;
          
          if (_selectedQuestionType == 'MCQ' && !isMCQ) return false;
          if (_selectedQuestionType == 'Theory' && !isTheory) return false;
        }
        
        // Subject filter
        if (_selectedSubject != 'All Subjects') {
          final subjectMatch = _formatSubjectName(collection.subject) == _selectedSubject;
          if (!subjectMatch) return false;
        }
        
        // Topic filter
        if (_selectedTopic != 'All Topics') {
          // Check collection-level topic (in description field for saved collections)
          bool hasCollectionTopic = collection.description == _selectedTopic;
          
          // Also check question-level topics
          final collectionTopics = collection.questions
              .expand((q) => q.topics)
              .toSet()
              .toList();
          bool hasQuestionTopic = collectionTopics.contains(_selectedTopic);
          
          if (!hasCollectionTopic && !hasQuestionTopic) return false;
        }
        
        // Year filter
        if (_selectedYear != 'All Years') {
          if (collection.year != _selectedYear) return false;
        }
        
        // Search filter
        if (_searchController.text.isNotEmpty) {
          final search = _searchController.text.toLowerCase();
          final displayText = (collection.title.isNotEmpty ? collection.title : collection.displayName).toLowerCase();
          if (!displayText.contains(search)) return false;
        }
        
        return true;
      }).toList();
    });
  }

  String _formatSubjectName(Subject subject) {
    switch (subject) {
      case Subject.mathematics: return 'Mathematics';
      case Subject.english: return 'English';
      case Subject.integratedScience: return 'Integrated Science';
      case Subject.socialStudies: return 'Social Studies';
      case Subject.ga: return 'Ga';
      case Subject.asanteTwi: return 'Asante Twi';
      case Subject.french: return 'French';
      case Subject.ict: return 'ICT';
      case Subject.religiousMoralEducation: return 'Religious and Moral Education';
      case Subject.creativeArts: return 'Creative Arts';
      case Subject.careerTechnology: return 'Career Technology';
      case Subject.trivia: return 'Trivia';
    }
  }

  @override
  Widget build(BuildContext context) {
    final isSmallScreen = MediaQuery.of(context).size.width < 768;
    
    return Scaffold(
      backgroundColor: const Color(0xFFF8FAFE),
      body: _isLoading
          ? const Center(child: CircularProgressIndicator(color: Color(0xFFD62828)))
          : CustomScrollView(
              slivers: [
                SliverToBoxAdapter(
                  child: _buildFilters(isSmallScreen),
                ),
                SliverToBoxAdapter(
                  child: _filteredCollections.isEmpty
                      ? _buildEmptyState()
                      : _buildCollectionsList(isSmallScreen),
                ),
              ],
            ),
    );
  }

  Widget _buildFilters(bool isSmallScreen) {
    return Container(
      margin: EdgeInsets.all(isSmallScreen ? 16 : 24),
      padding: EdgeInsets.all(isSmallScreen ? 16 : 20),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(16),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withValues(alpha: 0.05),
            blurRadius: 10,
            offset: const Offset(0, 2),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Title
          Text(
            'Search & Filter',
            style: GoogleFonts.playfairDisplay(
              fontSize: 18,
              fontWeight: FontWeight.bold,
              color: const Color(0xFF1A1E3F),
            ),
          ),
          const SizedBox(height: 16),
          // Search bar
          TextField(
            controller: _searchController,
            onChanged: (_) => _applyFilters(),
            style: GoogleFonts.montserrat(color: const Color(0xFF1A1E3F)),
            decoration: InputDecoration(
              hintText: 'Search collections...',
              hintStyle: GoogleFonts.montserrat(color: Colors.grey[400]),
              prefixIcon: Icon(Icons.search, color: Colors.grey[600]),
